package exam03_operator;

public class Operator04_논리연산자2 {

	public static void main(String[] args) {

		int n = 10;
		int n2 = 3;
		
		// 실행결과를 변수에 저장 가능
		boolean result = n == n2;
		System.out.println(result);		
		
		System.out.println(n == n2); // n과 n2값이 같냐?
		System.out.println(n != n2); 
		System.out.println(n > n2); 
		System.out.println(n >= n2); 
		System.out.println(n < n2); 
		System.out.println(n <= n2); 

	
	}

}
