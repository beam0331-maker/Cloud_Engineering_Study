package exam03_operator;

public class Operator01_산술연산자 {

	public static void main(String[] args) {

		// 전치 연산자
		int n = 10; 
		++n; // n = n + 1 동일, n += 1 동일
		System.out.println(n);
		
		--n; // n = n - 1 동일, n -= 1 동일
		System.out.println(n);
		
		// 전치 연산자
		int n2 = 10; 
		n2++; // n = n + 1 동일, n += 1 동일
		System.out.println(n);
		
		n2--; // n = n - 1 동일, n -= 1 동일
		System.out.println(n);	
				

	}

}
