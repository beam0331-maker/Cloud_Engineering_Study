package exam06_제어문;

public class Staement05_for문 {

	public static void main(String[] args) {

		// hello 문자열 5번 출력

		for (int n = 0; n <= 4; n++) {
			System.out.println("hello");

		}
		System.out.println("===============================");

		for (int n = 10; n > 4; n--) {
			System.out.println("world" + n);

		}

	}

}
